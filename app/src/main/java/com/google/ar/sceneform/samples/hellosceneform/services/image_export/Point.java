package com.google.ar.sceneform.samples.hellosceneform.services.image_export;

import android.graphics.Color;

public class Point {
    float x;
    float y;
    float size;
    int color;

    public Point(float x, float y, float size, int color) {
        this.x = x;
        this.y = y;
        this.size = size;
        this.color = color;
    }
}
